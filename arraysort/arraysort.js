//array sort ascending
const hewan = ["harimau","bebek","angsa","kucing"];
    hewan.reverse();
    console.log(hewan);

//output
//[ 'kucing', 'angsa', 'bebek', 'harimau' ]

//array sort descending
const fruit = ["mangga","apel","pisang","anggur"];
    fruit.sort();

    console.log(fruit);

//output
//[ 'anggur', 'apel', 'mangga', 'pisang' ]
